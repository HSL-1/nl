def load_annual_inflow():
    """历年入库径流量数据"""
    records = []
    months = ["1月","2月","3月","4月","5月","6月","7月","8月","9月","10月","11月","12月"]
    for year, vals in _ANNUAL_INFLOW_DATA.items():
        row = {"年份": year}
        for i, m in enumerate(months):
            row[m] = vals[i] if i < len(vals) else 0
        records.append(row)
    df = pd.DataFrame(records)
    return df

def load_historical_dispatch(event_name):
    """
    加载历史调度数据（模拟数据，因原始数据量较大仅提供关键特征）
    实际课程设计中建议使用原始Excel数据
    """
    # 简化的模拟调度数据
    np.random.seed(hash(event_name) % 10000)
    n = 20
    times = [f"2024-01-{d+1:02d} 00:00" for d in range(n)]

    if "莫兰蒂" in event_name:
        levels = 198.5 - np.cumsum(np.random.rand(n) * 0.03)
        inflows = np.random.rand(n) * 30 + 10
        spills = np.random.rand(n) * 20
    elif "利奇马" in event_name:
        levels = 199.0 + np.cumsum(np.random.rand(n) * 0.05)
        inflows = np.random.rand(n) * 50 + 30
        spills = np.random.rand(n) * 40
    else:
        levels = 198.0 + np.cumsum(np.random.rand(n) * 0.02 - 0.005)
        inflows = np.random.rand(n) * 20 + 5
        spills = np.random.rand(n) * 10

    storage_func = get_storage_interpolator()
    storages = [float(storage_func(l)) for l in levels]

    df = pd.DataFrame({
        "时段": times,
        "末水位_m": np.round(levels, 2),
        "相应库容_万m3": np.round(storages, 1),
        "净入库流量_m3s": np.round(inflows, 1),
        "闸门流量_m3s": np.round(spills, 1),
    })
    return df


